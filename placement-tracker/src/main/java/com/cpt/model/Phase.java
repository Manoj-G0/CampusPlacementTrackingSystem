package com.cpt.model;

public class Phase {
	private int hphId;
	private String PhaseName;

	// getters & setters
	public int getHphId() {
		return hphId;
	}

	public void setHphId(int hphId) {
		this.hphId = hphId;
	}

	public String getPhaseName() {
		return PhaseName;
	}

	public void setPhaseName(String PhaseName) {
		this.PhaseName = PhaseName;
	}
}
