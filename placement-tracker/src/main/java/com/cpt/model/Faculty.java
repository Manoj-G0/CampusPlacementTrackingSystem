package com.cpt.model;

public class Faculty {
    private Integer facId;
    private String facName;
    private String facEmail;

    public Faculty() {}

    public Integer getFacId() { return facId; }
    public void setFacId(Integer facId) { this.facId = facId; }
    public String getFacName() { return facName; }
    public void setFacName(String facName) { this.facName = facName; }
    public String getFacEmail() { return facEmail; }
    public void setFacEmail(String facEmail) { this.facEmail = facEmail; }
}